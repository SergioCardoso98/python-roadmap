#Python Arithmetic Operators
#+ 	Addition 	x + y 	
#- 	Subtraction 	x - y 	
#* 	Multiplication 	x * y 	
#/ 	Division 	x / y 	
#% 	Modulus 	x % y 	
#** 	Exponentiation 	x ** y 	
#// 	Floor division 	x // y

#Python Assignment Operators
#Operator	Explanation
#=	Assignment operator: Assigns the value on the right to the variable on the left. Example: x = 5.
#+=	Addition assignment: Adds the right operand to the left and stores the result in the left operand. Example: x += 3 is the same as x = x + 3.
#-=	Subtraction assignment: Subtracts the right operand from the left and assigns the result. Example: x -= 3 is the same as x = x - 3.
#*=	Multiplication assignment: Multiplies both operands and stores the result in the left operand. Example: x *= 3.
#/=	Division assignment: Divides the left operand by the right and assigns the result. Example: x /= 3.
#%=	Modulus assignment: Takes the remainder of division and assigns the result. Example: x %= 3.
#//=	Floor division assignment: Performs integer division (rounds down) and assigns the result. Example: x //= 3.
#**=	Exponentiation assignment: Raises the left operand to the power of the right and assigns the result. Example: x **= 3.
#&=	Bitwise AND assignment: Performs bitwise AND and assigns the result. Example: x &= 3.
#`	=`
#^=	Bitwise XOR assignment: Performs bitwise XOR and assigns the result. Example: x ^= 3.
#>>=	Bitwise right shift assignment: Shifts bits to the right and assigns the result. Example: x >>= 3.
#<<=	Bitwise left shift assignment: Shifts bits to the left and assigns the result. Example: x <<= 3.
#:=	Walrus operator (assignment expression): Assigns a value to a variable as part of an expression. Requires Python 3.8+. Example: print(x := 3)


#Python Comparison Operators
#== 	Equal 	x == y 	
#!= 	Not equal 	x != y 	
#> 	Greater than 	x > y 	
#< 	Less than 	x < y 	
#>= 	Greater than or equal to 	x >= y 	
#<= 	Less than or equal to 	x <= y

#Python Identity Operators
#is  	Returns True if both variables are the same object 	x is y 	
#is not 	Returns True if both variables are not the same object 	x is not y

#Python Membership Operators
#in  	Returns True if a sequence with the specified value is present in the object 	x in y 	
#not in 	Returns True if a sequence with the specified value is not present in the object 	x not in y

#Python Bitwise Operators
#&  	AND 	Sets each bit to 1 if both bits are 1 	x & y 	
#| 	OR 	Sets each bit to 1 if one of two bits is 1 	x | y 	
#^ 	XOR 	Sets each bit to 1 if only one of two bits is 1 	x ^ y 	
#~ 	NOT 	Inverts all the bits 	~x 	
#<< 	Zero fill left shift 	Shift left by pushing zeros in from the right and let the leftmost bits fall off 	x << 2 	
#>> 	Signed right shift 	Shift right by pushing copies of the leftmost bit in from the left, and let the rightmost bits fall off 	x >> 2
